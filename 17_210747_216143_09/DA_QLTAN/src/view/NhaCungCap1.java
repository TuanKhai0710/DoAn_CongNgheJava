    public boolean isValidPhoneNumber() {
        String phoneNumber = txt_SDT.getText();
        // Định dạng số điện thoại: 10 chữ số và bắt đầu từ số 0
        String regex = "^0[0-9]{9}$";
        
        // Biên dịch regex
        Pattern pattern = Pattern.compile(regex);
        
        // So khớp regex với chuỗi số điện thoại
        Matcher matcher = pattern.matcher(phoneNumber);
        
        // Trả về kết quả so khớp
        return matcher.matches();
    }
     private boolean kiemTraNhapDayDu() {
    String MaNCC = txt_MaNCC.getText().trim();
    String TenNCC = txt_tenNCC.getText().trim();
    String DiaChi = txt_DiaChi.getText().trim();
    String Sdt = txt_SDT.getText().trim();

    if (MaNCC.isEmpty() || TenNCC.isEmpty() || DiaChi.isEmpty() || Sdt.isEmpty()) {
        JOptionPane.showMessageDialog(btn_Luu, "Vui lòng nhập đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    return true;
    }
