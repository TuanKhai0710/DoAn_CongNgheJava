     private void HienthiTable() {
    if (ds_List != null && tbl_NCC != null) {
        String[] Hearder = {"MaNCC", "TenNCC", "SDT", "DiaChi"};
        DefaultTableModel model = new DefaultTableModel(Hearder, 0);
        
        for (NhaCungCapClass c : ds_List) {
            Object[] o = {c.MaNCC, c.TenNCC, c.SDT, c.DiaChi};
            model.addRow(o);
        }
        
        tbl_NCC.setModel(model);
        
        if (vitri >= 0 && vitri < model.getRowCount()) {
            tbl_NCC.setRowSelectionInterval(vitri, vitri);
        } else {
            tbl_NCC.clearSelection(); // Clear selection if vitri is out of range
        }
    } else {
        System.out.println("Error: ds_List or tbl_NCC is null.");
    }
    }
