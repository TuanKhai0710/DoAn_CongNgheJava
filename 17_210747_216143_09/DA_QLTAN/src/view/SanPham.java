    public int isGiaNhapNhoHonGiaSP() {
        try {
            int giaNhap = Integer.parseInt(txt_GiaNhap.getText());
            int giaSP = Integer.parseInt(txt_GiaSP.getText());

            // Kiểm tra nếu giá nhập nhỏ hơn hoặc bằng 0
            if (giaNhap <= 0) {
                return -1; // Giá nhập nhỏ hơn hoặc bằng 0
            }

            // So sánh giá nhập và giá sản phẩm
            if (giaNhap < giaSP) {
                return 1; // Giá nhập nhỏ hơn giá sản phẩm
            } else {
                return 0; // Giá nhập không nhỏ hơn giá sản phẩm
            }
        } catch (NumberFormatException e) {
            return -2; // Giá trị nhập vào không hợp lệ
        }
    }


