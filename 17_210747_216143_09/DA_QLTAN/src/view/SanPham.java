    private void HienthiTable() {
        if (ds_List != null && tbl_SanPham != null) {
            String[] Hearder = {"MaSP", "MALOAISP", "TenSP", "GiaNhap", "GiaSP", "HinhAnh"};
            DefaultTableModel model = new DefaultTableModel(Hearder, 0);

            for (SanPhamClass c : ds_List) {
                Object[] o = {c.MaSP, c.MaLoaiSP, c.TenSP, c.GiaNhap, c.GiaSP, c.HinhAnh};
                model.addRow(o);
            }

            tbl_SanPham.setModel(model);

            if (vitri >= 0 && vitri < model.getRowCount()) {
                tbl_SanPham.setRowSelectionInterval(vitri, vitri);
            } else {
                tbl_SanPham.clearSelection(); // Clear selection if vitri is out of range
            }
        } else {
            System.out.println("Error: ds_List or tbl_SanPham is null.");
        }
    }

    
