    private void btn_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LuuActionPerformed
        // TODO add your handling code here:
        if (kiemTraNhapDayDu()) {
            int checkResult = isGiaNhapNhoHonGiaSP();
            if (checkResult == -1) {
                JOptionPane.showMessageDialog(btn_Luu, "Giá nhập phải lớn hơn 0.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (checkResult == 0) {
                JOptionPane.showMessageDialog(btn_Luu, "Giá nhập phải nhỏ hơn giá bán.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            } else if (checkResult == -2) {
                JOptionPane.showMessageDialog(btn_Luu, "Giá trị nhập vào không hợp lệ.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String message = "Bạn có chắc chắn muốn ";
            if (previousAction.equals("Them")) {
                message += "thêm không?";
            }
            if (previousAction.equals("Xoa")) {
                message += "xóa không?";
            }
            if (previousAction.equals("Sua")) {
                message += "sửa không?";
            }

            int confirm = JOptionPane.showConfirmDialog(btn_Luu, message, "Xác nhận", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION)
            {
                SanPhamClass c = Chuyendoitextbox_sangdoituong();
                if( flag == 1 )
                {
                    dssp.ThemSanPham(c);
                    flag = 0; //Để bấm các tính năng khác
                    XoaTextBox();
                    AnHienButton(true); //2. Nút lưu khóa, các nút khác hiện.
                    AnHienTextBox(false); //Khóa textbox lại cho người dùng không sửa bậy
                    //lbThongBao.setText("Hãy chọn 1 thao tác");
                    JOptionPane.showMessageDialog(btn_Luu, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

                if (flag == 2 )
                {
                    dssp.SuaSanPham(c);
                    flag= 0;
                    XoaTextBox();
                    AnHienButton(true); //2. Nút lưu khóa, các nút khác hiện.
                    AnHienTextBox(false); //Khóa textbox lại cho người dùng không sửa bậy
                    //lbThongBao.setText("Hãy chọn 1 thao tác");
                    JOptionPane.showMessageDialog(btn_Luu, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                }

                if (flag == 3)
                {
                    dssp.XoaSanPham(c);
                    XoaTextBox();
                        JOptionPane.showMessageDialog(btn_Luu, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                dssp = new daoSanPham();
                ds_List = dssp.LaydsSanPham();
                HienthiTable();
            }
        }
    }//GEN-LAST:event_btn_LuuActionPerformed

