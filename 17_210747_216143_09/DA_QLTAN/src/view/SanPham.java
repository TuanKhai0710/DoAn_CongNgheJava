    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String giaNhapStr = txt_GiaNhap.getText().trim();
    
    // Kiểm tra xem giá nhập có giá trị hợp lệ không
    if (!giaNhapStr.isEmpty()) {
        try {
            // Chuyển đổi giá nhập thành số
            int giaNhap = Integer.parseInt(giaNhapStr);
            
            // Tính toán giá bán mới bằng cách nhân giá nhập với 1.5
            int giaBan = giaNhap * 2;
            
            // Cập nhật giá bán mới lên text field của giá bán
            txt_GiaSP.setText(String.valueOf(giaBan));
        } catch (NumberFormatException e) {
            // Xử lý nếu giá trị nhập không phải là số
            JOptionPane.showMessageDialog(this, "Giá nhập không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        } else {
            // Xử lý nếu giá nhập không được nhập vào
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giá nhập", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

