    private boolean kiemTraNhapDayDu() {
    String maLoaiSP = txt_MaLoai.getText().trim();
    String tenLoaiSP = txt_tenLoai.getText().trim();

    if (maLoaiSP.isEmpty() || tenLoaiSP.isEmpty()) {
        JOptionPane.showMessageDialog(btn_Luu, "Vui lòng nhập đầy đủ thông tin cho Mã Loại SP và Tên Loại SP", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    return true;
    }
