    private void HienthiTable() {
    if (ds_List != null && tbl_LoaiSP13 != null) {
        String[] Hearder = {"Mã loại", "Tên loại"};
        DefaultTableModel model = new DefaultTableModel(Hearder, 0);
        
        for (LoaiSPClass c : ds_List) {
            Object[] o = {c.MaLoaiSP, c.LoaiSP};
            model.addRow(o);
        }
        
        tbl_LoaiSP13.setModel(model);
        
        if (vitri >= 0 && vitri < model.getRowCount()) {
            tbl_LoaiSP13.setRowSelectionInterval(vitri, vitri);
        } else {
            tbl_LoaiSP13.clearSelection(); // Clear selection if vitri is out of range
        }
    } else {
        System.out.println("Error: ds_List or tbl_LoaiSP is null.");
    }
}
