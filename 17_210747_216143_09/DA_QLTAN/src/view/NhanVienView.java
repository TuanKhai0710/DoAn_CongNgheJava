    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (kiemTraNhapDayDu())
        {
            if ((flag ==1 || flag ==2) && !isValidPhoneNumber()) {
                JOptionPane.showMessageDialog(btnLuu, "Bạn nhập sai định dạng Số điện thoại", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String message = "Bạn có chắc chắn muốn ";
            if (previousAction.equals("Them")) {
                message += "thêm không?";
            }
            if (previousAction.equals("Xoa")) {
                message += "xóa không?";
            }
            if (previousAction.equals("Sua")) {
                message += "sửa không?";
            }

            int confirm = JOptionPane.showConfirmDialog(btnLuu, message, "Xác nhận", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION)
            {
                try {
                    NhanVien c = Chuyendoitextbox_sangdoituong();
                    if( flag == 1 )
                    {

                        dsnv.ThemNV(c);
                        flag= 0; //Để bấm các tính năng khác
                        XoaTextBox();
                        AnHienButton(true); //2. Nút lưu khóa, các nút khác hiện.
                        AnHienTextBox(false); //Khóa textbox lại cho người dùng không sửa bậy
                        //lbThongBao.setText("Hãy chọn 1 thao tác");
                        JOptionPane.showMessageDialog(btnLuu, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE); //cái này thêm ko dc nó vẫn chạy !!!
                    }

                    if (flag == 2 )
                    {
                        dsnv.SuaNV(c);
                        flag= 0;
                        XoaTextBox();
                        AnHienButton(true); //2. Nút lưu khóa, các nút khác hiện.
                        AnHienTextBox(false); //Khóa textbox lại cho người dùng không sửa bậy
                        //lbThongBao.setText("Hãy chọn 1 thao tác");
                        JOptionPane.showMessageDialog(btnLuu, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    }

                    if (flag == 3)
                    {
                        dsnv.XoaNV(c);
                        XoaTextBox();
                        //Khi xóa xong, 1. Nút lưu vẫn mở, các nút khác vẫn khóa (cho ng dùng xóa tiếp đôi tg khác. (cho đến khi ấn hủy). 2.
                            //anHienText(true); //khi xóa xong thì vẫn còn hiện
                            JOptionPane.showMessageDialog(btnLuu, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        }

                        //flag= 0;
                        //xoaTextBox();
                        dsnv = new DAO_NhanVien();
                        ds_List = dsnv.LaydsNV();
                        HienthiTable();
                        //anHienText(true);
                    } catch (ParseException ex) {
                        Logger.getLogger(NhanVienView.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
    }//GEN-LAST:event_btnLuuActionPerformed

