    private void HienthiTable() {
    if (ds_List != null && tbl_NV != null) {
        String[] Hearder = {"MaNV", "TenNV", "Chức vụ", "SĐT", "NVL", "GT", "Lương"};
        DefaultTableModel model = new DefaultTableModel(Hearder, 0);
        
        for (NhanVien c : ds_List) {
            Object[] o = {c.MaNV, c.TenNV, c.ChucVu, c.SDT, c.NVL, c.gt, c.Luong};
            model.addRow(o);
        }
        
        tbl_NV.setModel(model);
        
        if (vitri >= 0 && vitri < model.getRowCount()) {
            tbl_NV.setRowSelectionInterval(vitri, vitri);
        } else {
            tbl_NV.clearSelection(); // Clear selection if vitri is out of range
        }
    } else {
        System.out.println("Error: ds_List or tbl_LoaiSP is null.");
    }
    }
